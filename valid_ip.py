import re

regex='''^(25[0-5]|2[0-4][0-9]|[0-1]?[0-9][0-9]?)\.(
           25[0-5]|2[0-4][0-9]|[0-1]?[0-9][0-9]?)\.(
           25[0-5]|2[0-4][0-9]|[0-1]?[0-9][0-9]?)\.(
           25[0-5]|2[0-4][0-9]|[0-1]?[0-9][0-9]?)'''

def check(ip):
    if(re.search(regex,ip)):
        print("it is valid ip")
    else:
        print("it is invalid")


check("110.234.52.124")

